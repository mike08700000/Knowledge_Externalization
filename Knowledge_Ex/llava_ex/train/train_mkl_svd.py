def find_target_token_positions(output_tokens, target_tokens):
    window_size = len(target_tokens)
    positions = []
    for i in range(len(output_tokens) - window_size + 1):
        if torch.equal(output_tokens[i:i + window_size], target_tokens):
            positions.append(i)
    return positions

